import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const SOURCE_URL =
  process.env.IPVERSE_AS_METADATA_URL ?? "https://raw.githubusercontent.com/ipverse/as-metadata/master/as.json";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const outputPath = path.resolve(__dirname, "datacenterAsns.ts");

async function loadSource() {
  const localPath = process.argv[2];
  if (localPath) {
    return JSON.parse(await readFile(localPath, "utf8"));
  }

  const response = await fetch(SOURCE_URL);
  if (!response.ok) {
    throw new Error(`Failed to fetch ${SOURCE_URL}: ${response.status} ${response.statusText}`);
  }
  return response.json();
}

function uniqueSortedHostingAsns(entries) {
  if (!Array.isArray(entries)) {
    throw new Error("Expected ipverse AS metadata to be a JSON array");
  }

  const asns = new Set();
  for (const entry of entries) {
    const asn = entry?.asn;
    const category = entry?.metadata?.category;
    if (Number.isInteger(asn) && asn > 0 && category === "hosting") {
      asns.add(asn);
    }
  }

  return [...asns].sort((a, b) => a - b);
}

function formatAsnList(asns) {
  const lines = [];
  for (let i = 0; i < asns.length; i += 14) {
    lines.push(`  ${asns.slice(i, i + 14).join(", ")},`);
  }
  return lines.join("\n");
}

function render(asns) {
  const generatedAt = new Date().toISOString().slice(0, 10);
  return `/**
 * Generated by server/src/services/tracker/botBlocking/update-datacenter-asns.mjs.
 *
 * Source: ipverse/as-metadata as.json
 * URL: ${SOURCE_URL}
 * Criterion: metadata.category === "hosting"
 * Generated: ${generatedAt}
 *
 * Do not edit the ASN list by hand. Re-run:
 *   npm run update:datacenter-asns
 */

export const DATACENTER_ASN_SOURCE = {
  name: "ipverse/as-metadata",
  url: "${SOURCE_URL}",
  criterion: "metadata.category === hosting",
  generatedAt: "${generatedAt}",
} as const;

const DATACENTER_ASN_LIST: readonly number[] = [
${formatAsnList(asns)}
];

export const DATACENTER_ASN_COUNT = DATACENTER_ASN_LIST.length;

export const DATACENTER_ASNS: ReadonlySet<number> = new Set(DATACENTER_ASN_LIST);

export function isDatacenterAsn(asn: number | null | undefined): boolean {
  return typeof asn === "number" && DATACENTER_ASNS.has(asn);
}
`;
}

const source = await loadSource();
const asns = uniqueSortedHostingAsns(source);
if (asns.length < 1000) {
  throw new Error(`Refusing to write suspiciously small ASN list (${asns.length} entries)`);
}

await writeFile(outputPath, render(asns));
console.log(`Wrote ${asns.length} hosting ASNs to ${path.relative(process.cwd(), outputPath)}`);
