import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const SOURCE_URL =
  process.env.REFERRER_SPAM_URL ??
  "https://raw.githubusercontent.com/matomo-org/referrer-spam-list/master/spammers.txt";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const outputPath = path.resolve(__dirname, "referrerSpam.ts");

async function loadSource() {
  const localPath = process.argv[2];
  if (localPath) {
    return readFile(localPath, "utf8");
  }

  const response = await fetch(SOURCE_URL);
  if (!response.ok) {
    throw new Error(`Failed to fetch ${SOURCE_URL}: ${response.status} ${response.statusText}`);
  }
  return response.text();
}

function uniqueSortedDomains(text) {
  const domains = new Set();
  for (const rawLine of text.split(/\r?\n/)) {
    const line = rawLine.trim().toLowerCase();
    if (!line || line.startsWith("#")) continue;
    // Strip any accidental scheme / path / port the upstream list might pick up.
    const withoutScheme = line.replace(/^https?:\/\//, "");
    const host = withoutScheme.split("/")[0].split(":")[0].trim();
    if (host.length === 0) continue;
    domains.add(host);
  }
  return [...domains].sort();
}

function formatDomainList(domains) {
  const lines = [];
  for (let i = 0; i < domains.length; i += 4) {
    const chunk = domains.slice(i, i + 4).map(d => JSON.stringify(d));
    lines.push(`  ${chunk.join(", ")},`);
  }
  return lines.join("\n");
}

function render(domains) {
  const generatedAt = new Date().toISOString().slice(0, 10);
  return `/**
 * Generated by server/src/services/tracker/botBlocking/update-referrer-spam.mjs.
 *
 * Source: matomo-org/referrer-spam-list spammers.txt
 * URL: ${SOURCE_URL}
 * Generated: ${generatedAt}
 *
 * Do not edit the domain list by hand. Re-run:
 *   npm run update:referrer-spam
 */

export const REFERRER_SPAM_SOURCE = {
  name: "matomo-org/referrer-spam-list",
  url: "${SOURCE_URL}",
  generatedAt: "${generatedAt}",
} as const;

const REFERRER_SPAM_LIST: readonly string[] = [
${formatDomainList(domains)}
];

export const REFERRER_SPAM_COUNT = REFERRER_SPAM_LIST.length;

export const REFERRER_SPAM_DOMAINS: ReadonlySet<string> = new Set(REFERRER_SPAM_LIST);

/**
 * Extract a lowercase hostname from a referrer value. Accepts either a full
 * URL ("https://example.com/path") or a bare host ("example.com"). Returns
 * null if no host can be extracted.
 */
function extractHost(referrer: string): string | null {
  const trimmed = referrer.trim().toLowerCase();
  if (trimmed.length === 0) return null;
  try {
    return new URL(trimmed).hostname || null;
  } catch {
    // Not a full URL — treat as bare host.
    const host = trimmed.replace(/^https?:\\/\\//, "").split("/")[0].split(":")[0];
    return host || null;
  }
}

/**
 * Check whether a referrer value is from a known spam domain. Matches the
 * exact hostname and any subdomain of a listed host (e.g. "promo.semalt.com"
 * matches "semalt.com").
 */
export function isReferrerSpam(referrer: string | null | undefined): boolean {
  if (typeof referrer !== "string") return false;
  const host = extractHost(referrer);
  if (!host) return false;
  if (REFERRER_SPAM_DOMAINS.has(host)) return true;
  // Match parent domains: walk up labels until we find a match or run out.
  let dotIndex = host.indexOf(".");
  while (dotIndex >= 0) {
    const parent = host.slice(dotIndex + 1);
    if (REFERRER_SPAM_DOMAINS.has(parent)) return true;
    dotIndex = host.indexOf(".", dotIndex + 1);
  }
  return false;
}
`;
}

const source = await loadSource();
const domains = uniqueSortedDomains(source);
if (domains.length < 500) {
  throw new Error(`Refusing to write suspiciously small referrer spam list (${domains.length} entries)`);
}

await writeFile(outputPath, render(domains));
console.log(`Wrote ${domains.length} referrer spam domains to ${path.relative(process.cwd(), outputPath)}`);
